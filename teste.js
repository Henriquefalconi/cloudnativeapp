// Definindo uma função para verificar se um número é par
function isEven(number) {
  return number % 2 === 0;
}

// Testando a função com alguns valores
console.log(isEven(10)); // deve retornar true
console.log(isEven(16)); // deve retornar false
console.log(isEven(11)); // deve retornar true
console.log(isEven(167445)); // deve retornar false


// Este código define uma função isEven que recebe um número como argumento e retorna true se o número for par e false caso contrário. 
